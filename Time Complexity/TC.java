// Time Complexity (TC) is a measure of the amount of time an algorithm 
// takes to complete as a function of the size of the input. 
// It is typically expressed using Big O notation, 
// which describes the upper bound of the growth rate of the algorithm's running time. 
// Common time complexities include O(1) for constant time,
// O(log n) for logarithmic time, O(n) for linear time,
// O(n log n) for linearithmic time, and O(n^2) for quadratic time. 
// Understanding time complexity is crucial for analyzing and optimizing algorithms to ensure they run efficiently.

// How to calculate time complexity?
// 1. Identify the basic operations: Determine the fundamental operations that contribute to the running time
//    of the algorithm, such as comparisons, assignments, or function calls.
// 2. Count the number of operations: Analyze how many times each basic operation is executed
//    as a function of the input size (n). This may involve loops, recursive calls, or other constructs.
// 3. Express the total number of operations: Combine the counts from step 2 to
//    express the total number of operations as a function of n.
// 4. Simplify the expression: Use Big O notation to simplify the expression by keeping
//    only the dominant term and ignoring constant factors and lower-order terms.
// - Avoid constants: When expressing time complexity, ignore constant factors as they do not affect the growth rate.
// - Focus on the dominant term: Identify the term that grows the fastest as n increases and

